
import UIKit

public struct Color {
    static let accent = UIColor(red: 0, green: 0.65, blue: 0.64, alpha: 1)
}

// MARK: - Titles

public extension Color {
    
     enum title {
         static let `default` =  UIColor(red: 1, green: 1, blue: 1, alpha: 1)
    }
}

// MARK: - Buttons

public extension Color {
    
     enum button {
         static let `default` = UIColor(red: 0, green: 0.48, blue: 1, alpha: 1)
         static let plainTitle = UIColor(red: 0, green: 0.48, blue: 1, alpha: 1)
    }
}
